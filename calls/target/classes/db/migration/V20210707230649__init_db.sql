
CREATE TABLE public.call (
    id bigint NOT NULL,
    callee_number character varying(255) NOT NULL,
    caller_number character varying(255) NOT NULL,
    end_time timestamp,
    start_time timestamp,
    status character varying(255),
    type character varying(255) NOT NULL
);

ALTER TABLE public.call ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY (
    SEQUENCE NAME public.call_id_seq
    START WITH 8
    INCREMENT BY 1
    NO MINVALUE
    NO MAXVALUE
    CACHE 1
);

ALTER TABLE ONLY public.call
    ADD CONSTRAINT call_pkey PRIMARY KEY (id);


